def sum(n1, n2):
    """
    Add one number to the other

    :param n1: First number
    :type n1: float
    :param n2: Second number
    :type n2: float
    :return: sum of two arguments
    :rtype: float

    :Example:

    >>> sum(1, 2)
    3
    >>> sum(-1, 100)
    99

    """
    return n1 + n2


def subtract(n1, n2):
    """
    Subtract one number to the other

    :param n1: First number
    :type n1: float
    :param n2: Second number
    :type n2: float
    :return: subtraction of two arguments
    :rtype: float

    :Example:

    >>> subtract(1, 2)
    -1
    >>> subtract(-1, 100)
    -101

    """
    return n1 - n2
